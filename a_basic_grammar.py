# 1. 基本数据类型(整数-int, 浮点数-float, 字符串-str, 布尔值-bool)
# 1.1 整数 int
print(type(1))
print(type(2))
# 1.2 浮点数 float
print(type(1.1))
print(type(2.0))
# 1.3 字符串 str
print(type('python'))
print(type("python"))
# 1.4 布尔值
print(type(True))
print(type(False))
# 1.5 str转int
print(type(int('8')))
# 1.6 float转str
print(type(str(2.0)))
# 1.7 int转bool
print(bool(123))
print(bool(0))
print(bool(-321))

